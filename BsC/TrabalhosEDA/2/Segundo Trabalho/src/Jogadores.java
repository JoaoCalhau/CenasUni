public class Jogadores {
	private int jogador;
	private String jogada;
	
	public Jogadores(int jogador, String jogada) {
		this.jogador=jogador;
		this.jogada=jogada;
	}
	
	public int getJogador() {
		return this.jogador;
	}
	
	public String getJogada() {
		return this.jogada;
	}
}
